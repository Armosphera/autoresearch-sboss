def validate_uen(value: Any, *, kind_verifier=None) -> dict[str, Any]:
    """Validate a Singapore UEN. Returns { ok, normalized, error }.

    Args:
        value: raw input (string, None, or anything str()-coercible).
        kind_verifier: optional callable (str) -> bool. If supplied and returns False,
            validation fails with the kind error. Defaults to the local
            _default_kind (which currently accepts everything).

    Returns:
        { ok: bool, normalized: str, "error": str | None }
    """
    verifier = kind_verifier if kind_verifier is not None else _default_kind

    normalized = normalize_uen(value)
    if not normalized:
        return {"ok": False, "normalized": "", "error": "UEN is required"}
    if not _ALNUM_RE.match(normalized):
        return {"ok": False, "normalized": normalized, "error": "UEN must be alphanumeric"}
    if len(normalized) not in (UEN_MIN_LENGTH, UEN_MAX_LENGTH):
        return {"ok": False, "normalized": normalized, "error": f"UEN must be {UEN_MIN_LENGTH} or {UEN_MAX_LENGTH} characters, got {len(normalized)}"}
    if _ALL_SAME_RE.match(normalized):
        return {"ok": False, "normalized": normalized, "error": "UEN is invalid (all characters are the same)"}
    if not verifier(normalized):
        return {"ok": False, "normalized": normalized, "error": "UEN format is invalid"}
    return {"ok": True, "normalized": normalized, "error": None}


