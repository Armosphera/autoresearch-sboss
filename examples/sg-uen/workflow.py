"""
workflow.py — Singapore UEN (Unique Entity Number) validation.

This file is the agent's lever. The default implementation is intentionally WEAK — it only
does basic structural validation (9-10 alphanumeric, separator stripping). The agent's job:
add kind-based validation (local companies vs foreign vs limited partnerships) per the
ACRA spec (currently a documented TODO seam).

Source of truth: https://www.acra.gov.sg/ (Accounting and Corporate Regulatory Authority).
The UEN format and rules are public.

Reference format:
- 9-10 alphanumeric characters
- For local companies (most common): 9 digits, e.g. "201912345A" or "201912345B" (the
  last character is a check letter)
- For foreign companies: 10 digits
- For limited partnerships, societies, etc.: variable formats
- No mathematical check digit. The "check letter" at the end of 9-digit UENs is
  generated by ACRA's internal system, not by a public algorithm.

Special cases:
- "0000000000" (all zeros) → invalid
- All 9 digits the same → invalid
- Must start with a year prefix (19xx, 20xx, 21xx for new entities) — this is a soft
  check; old entities may not follow it

Real test cases (synthetic):
- 201912345A → valid (9 digits + check letter A)
- 20191234A → invalid (only 8 digits, not 9)
- 12345A6789 → invalid (mixed format, not standard)

The agent's job is to:
1. Implement format validation: 9-10 alphanumeric, with optional check letter at the end
2. Handle separator normalization (UENs are commonly written with hyphens or spaces)
3. Catch special invalid cases (all-zeros, all-same)
4. (Stretch) Add year-prefix validation: must start with 19/20/21
5. Return { ok, normalized, error } matching the eval_set contract
"""

from __future__ import annotations

import re
from typing import Any

UEN_MIN_LENGTH = 9
UEN_MAX_LENGTH = 10
_SEPARATOR_RE = re.compile(r"[\s\-]")
_ALNUM_RE = re.compile(r"^[A-Z0-9]+$")
_ALL_SAME_RE = re.compile(r"^(\w)\1{8,9}$")  # 9-10 of the same char
# Standard UEN: 9 digits + 1 check letter (total 10)
_DIGITS_AND_LETTER_RE = re.compile(r"^\d{9}[A-Z]$")
# Or 9-10 digits (no letter)
_DIGITS_ONLY_RE = re.compile(r"^\d{9,10}$")


def normalize_uen(value: Any) -> str:
    """Strip whitespace + hyphens, uppercase. Returns "" for null/None.

    Examples:
      '201912345A'  -> '201912345A'
      '2019 12345 A' -> '201912345A'
      '2019-12345-A' -> '201912345A'
    """
    if value is None:
        return ""
    return _SEPARATOR_RE.sub("", str(value)).upper()


